// you can use string and switch only in 1.7 and later version
public class Switch {

	public static void main(String[] args) {
		String n="abc";
		switch(n) {
		case "abc":
			System.out.println("One");
			break;
		case "pqr":
			System.out.println("Two");
			break;
		case "efg":
			System.out.println("Three");
			break;
		default:
			System.out.println("No Match");
		}
	}
	

}
